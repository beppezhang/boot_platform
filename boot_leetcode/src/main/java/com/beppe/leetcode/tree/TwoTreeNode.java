package com.beppe.leetcode.tree;

public class TwoTreeNode {

    public int val;
    public TwoTreeNode left;
    public TwoTreeNode right;



    public TwoTreeNode(int val, TwoTreeNode left, TwoTreeNode right) {
        this.val = val;
        this.left = left;
        this.right = right;
    }

    public TwoTreeNode(int val) {
        this.val = val;
    }

    public TwoTreeNode() {
    }
}
